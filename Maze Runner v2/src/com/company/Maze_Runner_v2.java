    public static void keepIn(int x) {
        if (bfsDriver)
            bfs.add(x);
        else
            dfs.push(x);
    }

    public static int takeOut() {
        if (bfsDriver)
            return bfs.poll();
        else
            return dfs.pop();
    }

    public static boolean isEmpty() {
        if (bfsDriver)
            return bfs.isEmpty();
        else
            return dfs.isEmpty();
    }

